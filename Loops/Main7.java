package Loops;
public class Main7 {
    public static void main(String[] args) {
        Main7 instance = new Main7();
        instance.loopingNumber();

    }

    // Write a program to print the numbers from 1 to 10 using a for loop
    public void loopingNumber() {
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
